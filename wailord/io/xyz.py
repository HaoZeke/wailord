# -*- coding: utf-8 -*-
"""XYZ I/O for wailord.

Prefers the chemparseplot grammar track (``chemparseplot[grammar]``). Falls
back to a local parsimonious grammar when that optional extra is missing so
existing call sites keep working during migration.

.. deprecated::
   Prefer ``chemparseplot.parse.grammar.parse_xyz_file`` (or
   ``chemparseplot.api.parse_xyz``) for new code.
"""

from __future__ import annotations

import itertools as itertt
import warnings
from collections import Counter
from pathlib import Path

from parsimonious.grammar import Grammar
from parsimonious.nodes import NodeVisitor

# Local fallback grammar (legacy). Prefer chemparseplot when available.
_grammar_xyz = Grammar(
    r"""
    meta = natoms ws coord_block ws?
    natoms = ~r"\d+"
    coord_block = (aline ws)+
    aline = (atype ws cline)
    atype = ~r"[A-Za-z][a-zA-Z0-9]{0,2}"
    cline = (float_ ws float_ ws float_)
    float_ = ~r"[+-]?(?:\d+\.\d*|\.\d+|\d+)(?:[eE][+-]?\d+)?"
    ws = ~r"\s*"
    """
)


class _xyzVisitor(NodeVisitor):
    """Legacy visitor for the local XYZ grammar."""

    def __init__(self):
        self.natoms = None
        self.coord_block = None
        self.clines = []
        self.atom_types = []
        self.meta = None

    def __repr__(self):
        return f"{self.meta}"

    def visit_meta(self, node, visited_children):
        self.meta = node.text
        return node.text

    def visit_coord_block(self, node, visited_children):
        cb = node.text.split("\n")
        for i, aline in enumerate(cb):
            each = aline.split()
            cb[i] = "    ".join(each)
        self.coord_block = "\n".join(cb)
        return node.text

    def visit_cline(self, node, visited_children):
        self.clines = node.text
        return node.text

    def visit_natoms(self, node, visited_children):
        self.natoms = int(node.text)
        return self.natoms

    def visit_atype(self, node, visited_children):
        self.atom_types.append(node.text)
        return node.text

    def generic_visit(self, node, visited_children):
        return node.text or visited_children


def _parse_via_chemparseplot(path: Path):
    """Return a chemparseplot XyzFrame or None if grammar track unavailable."""
    try:
        from chemparseplot.parse.grammar.xyz import parse_xyz_file
    except ImportError:
        return None
    return parse_xyz_file(path)


class _XyzDatCompat:
    """Legacy-shaped xyzdat for chemparseplot-backed reads.

    Callers (``inpGenerator``) expect ``.coord_block`` (atom lines only) and
    ``.atom_types`` (symbol list), matching the old ``_xyzVisitor`` surface.
    """

    def __init__(self, natoms: int, coord_block: str, atom_types: list[str], meta: str):
        self.natoms = natoms
        self.coord_block = coord_block
        self.atom_types = list(atom_types)
        self.meta = meta

    def __repr__(self):
        return f"{self.meta}"

    def __str__(self):
        return self.meta


class xyzIO:
    """XYZ file handler (proxy over chemparseplot grammar when installed)."""

    def __init__(self, filename):
        import warnings

        warnings.warn(
            "wailord.io.xyz.xyzIO is deprecated for new code; "
            "use chemparseplot.api.parse_xyz / chemparseplot.parse.grammar.xyz "
            "(optional chemparseplot[grammar]).",
            DeprecationWarning,
            stacklevel=2,
        )
        self.filename = Path(filename)
        self.comment_line = "Generated by wailord"
        self.xyzdat = None
        self.slug = None
        self.system = None
        self.counts = None
        self._atoms = None  # list of (symbol, x, y, z) when using suite path
        self._coord_lines = None  # original coordinate lines for exact write
        self._backend = "legacy"
        self.read()

    def __repr__(self):
        return f"XYZ file {self.filename}"

    def read(self):
        raw_lines = self.filename.read_text().splitlines()
        frame = _parse_via_chemparseplot(self.filename)
        if frame is not None:
            self._backend = "chemparseplot"
            # Match legacy readlines()[1] (includes trailing newline)
            self.comment_line = (
                raw_lines[1] + "\n" if len(raw_lines) > 1 else frame.comment
            )
            self.counts = Counter(frame.symbols)
            self.system = frame.system
            self.slug = f"{self.system}_{self.filename.stem}"
            self._atoms = [(a.symbol, a.x, a.y, a.z) for a in frame.atoms]
            self._coord_lines = raw_lines[2:] if len(raw_lines) > 2 else []
            # Prefer original formatting for coord_block (inpGenerator embeds it)
            if self._coord_lines:
                cb_lines = []
                for aline in self._coord_lines:
                    each = aline.split()
                    if each:
                        cb_lines.append("    ".join(each))
                coord_block = "\n".join(cb_lines)
            else:
                coord_block = "\n".join(
                    f"{s}    {x}    {y}    {z}" for s, x, y, z in self._atoms
                )
            meta_lines = [str(frame.natoms)] + [
                f"{s} {x} {y} {z}" for s, x, y, z in self._atoms
            ]
            self.xyzdat = _XyzDatCompat(
                natoms=int(frame.natoms),
                coord_block=coord_block,
                atom_types=list(frame.symbols),
                meta="\n".join(meta_lines),
            )
            return

        with open(self.filename) as fp:
            dat = fp.readlines()
            self.comment_line = dat[1]
            dat.pop(1)  # Kill comment line
            dat = "".join(map(str, dat))
            sx = _xyzVisitor()
            tree = _grammar_xyz.parse(dat)
            sx.visit(tree)
            self.xyzdat = sx
            self.counts = Counter(sx.atom_types)
            ldict = list(itertt.chain.from_iterable(self.counts.items()))
            self.system = "".join(map(str, ldict))
            self.slug = f"{self.system}_{self.filename.stem}"

    @property
    def comment_line(self):
        return self._comment_line

    @comment_line.setter
    def comment_line(self, cl):
        # Preserve legacy behavior: only collapse *internal* multi-line comments
        # (a single trailing newline from readlines must remain).
        if isinstance(cl, str):
            parts = cl.split("\n")
            nonempty = [p for p in parts if p != ""]
            if len(nonempty) > 1:
                cl = " ".join(nonempty)
                if parts and parts[-1] == "":
                    cl = cl + "\n"
        self._comment_line = cl

    def write(self, outname):
        if self._backend == "chemparseplot" and self._coord_lines is not None:
            natoms = (
                str(len(self._atoms))
                if self._atoms is not None
                else (self._coord_lines and "0")
            )
            # Preserve original atom-line formatting for byte-identical rewrite
            comment = str(self.comment_line)
            if not comment.endswith("\n"):
                comment = comment + "\n"
            # XYZ: natoms\n comment\n coords...  (comment already has newline)
            out = f"{natoms}\n{comment}" + "\n".join(self._coord_lines)
            if self._coord_lines and not out.endswith("\n"):
                out += "\n"
            # Original fixtures may omit final newline after last atom; match source
            src = self.filename.read_bytes()
            if not src.endswith(b"\n") and out.endswith("\n"):
                out = out[:-1]
            elif src.endswith(b"\n") and not out.endswith("\n"):
                out += "\n"
            Path(outname).write_text(out)
            return
        with open(outname, "w") as op:
            outdat = str(self.xyzdat).split("\n")
            outdat.insert(1, f"{str(self.comment_line).strip()}")
            op.write("\n".join(outdat))


# Back-compat names used by older imports / docs
grammar_xyz = _grammar_xyz
xyzVisitor = _xyzVisitor
